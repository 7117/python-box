# Numbers（数字）
# String（字符串）
# List（列表）
# Tuple（元组）
# Dictionary（字典）
a = '今天天气好啊'
print(a)

money = 5000
print(money)

money = 1.2
print(money)

sex = True
print(sex)

str = '单引号'
print(str)
print(str[0:2])

str = "双引号"
print(str)

str = '''三引号
换一行'''
print(str)

str = '"单引号嵌套双引号"'
print(str)

str = "'双引号嵌套单引号'"
print(str)

# 列表 当有很多数据，可以存储在列表中
name_list = ['整周', '对的', 2]
print(name_list)
print(type(name_list))

# 元组
age_tuple = (19, 29, 22, 22, '三岁')
print(age_tuple)
print(type(age_tuple))

# 字典
person = {'name': '孙潇', 'age': 22}
print(person)
print(type(person))
